x = float(input('Digite o 1° número: '))
y = int(input('Digite o 2° número: '))

soma = x + y
concanetar = str(x)+str(y)
mult = x*y
concanetar_str = str(x) * y 
divisao = x / y 
divisao_int = x // y 
exponeciacao = x ** y 
modulo = x % y 

print('A soma dos números é: ',soma)
print('A concanetação dos números é: ', concanetar)
print('A multiplicação dos números é: ', mult)
print('A concanetação de strings dos números é: ', concanetar_str)
print('A divisão dos números é: ', divisao)
print('A divisão inteira dos números é: ', divisao_int)
print('A exponeciação dos números é: ', exponeciacao)
print('O resto da divisão é: ', modulo)